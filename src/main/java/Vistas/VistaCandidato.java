    private void llenarCampos(){
        int fila = this.tblCanditados1.getSelectedRow();
        //int id = Integer.parseInt(this.tblCanditados1.getModel().getValueAt(selectedRow, 0).toString());
        String id = this.tblCanditados1.getValueAt(fila, 0).toString();
        //this.txtID.setText(String.valueOf(id));
        
        ClsCandidato candidato = this.controladorCandidato.recuperarCandidato(id);
        if (candidato != null){
            this.txtIdentificacion.setText(candidato.getNumeroDocumento());
            this.txtNombre.setText(candidato.getNombre());
            this.txtTelefono.setText(candidato.getTelefono());
            this.txtEmail.setText(candidato.getEmail());
            this.cboxCiudad.setSelectedItem(candidato.getCiudadorigen());
            this.cboxTipoDoc.setSelectedItem(candidato.getTipoDocumento());
            this.txtMensaje.setText(candidato.getMensajecampania());
            this.cboxPartidoPolitico.setSelectedItem(candidato.getPartidopolitico());
            this.txtDescripcion.setText(candidato.getDescripcion());
            this.txtMensaje.setText(candidato.getMensajecampania());
            this.cboxCargos.setSelectedItem(candidato.getCargo());
        }
    } 
    
