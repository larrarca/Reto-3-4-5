   public void ObtenerCandidatos(){
        LinkedList<ClsCandidato> listaCandidatos = this.controladorCandidato.ObtenerCandidatos();
        this.actualizarTabla(listaCandidatos);
    } 

    private void actualizarTabla(LinkedList<ClsCandidato> candidatos){
        DefaultTableModel modelo1 = (DefaultTableModel) this.tblCanditados1.getModel();
        modelo1.setRowCount(0);
        for (ClsCandidato c : candidatos){
            Object[] fila ={c.getNumeroDocumento(),  c.getNombre(),c.getPartidopolitico()};
            modelo1.addRow(fila);
        }
        this.tblCanditados1.getColumnModel().getColumn(0).setMinWidth(0);
        this.tblCanditados1.getColumnModel().getColumn(0).setMaxWidth(0);
        this.tblCanditados1.getColumnModel().getColumn(0).setWidth(0);
    }
   

