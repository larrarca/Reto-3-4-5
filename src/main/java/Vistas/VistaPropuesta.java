    private void llenarCampos(){
        int fila = this.tblPropuestas.getSelectedRow();
        //int id = Integer.parseInt(this.tblCanditados1.getModel().getValueAt(selectedRow, 0).toString());
        String idpropuesta = this.tblPropuestas.getValueAt(fila, 0).toString();
        String idcandidato = this.tblPropuestas.getValueAt(fila, 3).toString();
        //this.txtID.setText(String.valueOf(id));
        
        ClsPropuesta propuesta = this.controladorPropuesta.recuperarPropuesta(idpropuesta, idcandidato);
        if (propuesta != null){
            this.txtIDPropuesta.setText(propuesta.getIdPropuesta());
            this.cboxSector.setSelectedItem(propuesta.getSector());
            this.txtDescripcion.setText(propuesta.getDescripcion());
            this.txtIDCandidato.setText(propuesta.getIdCandidato());
        }
    }     

    
