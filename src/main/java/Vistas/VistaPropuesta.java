    public void ObtenerPropuestas(){
        String id = txtIDCandidato.getText();
        LinkedList<ClsPropuesta> listaPropuestas = this.controladorPropuesta.obtenerPropuestas(id);
        this.actualizarTabla(listaPropuestas);
    }   
    

    private void actualizarTabla(LinkedList<ClsPropuesta> propuestas){
        DefaultTableModel modelo1 = (DefaultTableModel) this.tblPropuestas.getModel();
        modelo1.setRowCount(0);
        for (ClsPropuesta c : propuestas){
            Object[] fila ={c.getIdPropuesta(),  c.getSector(),c.getDescripcion(),c.getIdCandidato()};
            modelo1.addRow(fila);
        }
        this.tblPropuestas.getColumnModel().getColumn(0).setMinWidth(0);
        this.tblPropuestas.getColumnModel().getColumn(0).setMaxWidth(0);
        this.tblPropuestas.getColumnModel().getColumn(0).setWidth(0);
        this.tblPropuestas.getColumnModel().getColumn(3).setMinWidth(0);
        this.tblPropuestas.getColumnModel().getColumn(3).setMaxWidth(0);
        this.tblPropuestas.getColumnModel().getColumn(3).setWidth(0);
        
    }       
    
    
    
